# assumes DATA is already defined exactly as in your message

def write_block_csv(block, out_file):
    """
    Write one block of DATA (e.g. DATA['response']) to CSV with format:

    SSTn_late,,SSTn_early,,SSTp_late,,SSTp_early,
    X,Y,X,Y,X,Y,X,Y
    ...
    """
    column_order = [
        ("negative", "late",  "SSTn_late"),
        ("negative", "early", "SSTn_early"),
        ("positive", "late",  "SSTp_late"),
        ("positive", "early", "SSTp_early"),
    ]

    arrays = [block[valence][timing] for valence, timing, _ in column_order]
    max_len = max(arr.shape[0] for arr in arrays)

    header1 = []
    header2 = []
    for _, _, label in column_order:
        header1.extend([label, ""])
        header2.extend(["X", "Y"])

    with open(out_file, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(header1)
        writer.writerow(header2)

        for i in range(max_len):
            row = []
            for arr in arrays:
                if i < len(arr):
                    row.extend([arr[i, 0], arr[i, 1]])
                else:
                    row.extend(["", ""])
            writer.writerow(row)

    print(f"Saved {out_file}")


